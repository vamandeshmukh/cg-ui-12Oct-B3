
let num = 10; // number 

let num2 = '10'; // string  

console.log(num == num2); // true because string  is casted to number 
console.log(num === num2); // false because type is compared first  // use this 

console.log(num != num2); // false because string  is casted to number 
console.log(num !== num2); // true because type is compared first  // use this 
